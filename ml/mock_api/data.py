    for user_id, user_data in USERS.items():
        for key, contact in user_data["contacts"].items():
            norm = _normalize_phone(contact["phone"])
            if norm and norm not in seen_phones:
                PHONE_INDEX[norm] = (user_id, key)
                seen_phones.add(norm)


